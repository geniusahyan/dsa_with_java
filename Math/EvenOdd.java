package Math;

public class EvenOdd {
    public static void main(String[] args) {
        int num = 10; // Change this value to test the program.
        if (num % 2 == 0) {
            System.out.println("The number " + num + " is even.");
        }else{
            System.out.println("The number " + num + " is odd.");
        }

    }
}
